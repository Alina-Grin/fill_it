#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*array;
	int		i;

	i = 0;
	c = (char)c;
	array = s;
	while (n--)
	{
		array[i] = c;
		i++;
	}
	return (s);
}
